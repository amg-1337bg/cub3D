#include "cub.h"

int		ft_atoi(const char *s)
{
	int neg;
	int res;

	res = 0;
	neg = 0;
	while (*s == 9 || *s == 32)
		s++;
	if (*s == '+' || *s == '-')
	{
		if (*s == '-')
			neg = 1;
		s++;
	}
	while (*s >= 48 && *s <= 57)
	{
		res = res * 10 + *s - '0';
		s++;
	}
	if (neg == 1)
		res *= -1;
	return (res);
}
